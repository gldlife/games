# 今天我们要学习python的输出命令，如何将指定的字符串打印到电脑屏幕上
# 输出命令的名称为：print()
# 我们只需要将要打印的语句用单引号''或者双引号" "包裹进来即可。
# 示例：
#     1. '我使用单引号' 对应的语句： print('我使用单引号')
#     2. "我使用双引号" 对应的语句： print("我使用双引号")
# 特别注意：
#     当我们所输出的语句中含有单引号，此时语句必需用双引号包裹；示例：" what's your name?"
#     当我们所输出的语句中含有双引号，此时语句必需用单引号包裹；示例：' 这个词"爱"是什么意思？'
print('Hello, my name is gldlife.')
print("What's your name?")